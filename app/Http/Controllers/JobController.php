        $this->validate($request, [
            'title' =>'required',
            'description' => 'required',
            'experience' => 'required',
            'vacancy' => 'required',
            'skill' => 'required'
        ]);
        $job = Job::create([
            'title' => $request->title,
            'description' => $request->description,
            'experience' => $request->experience,
            'vacancy' => $request->vacancy,
            'salary' => $request->salary,
            'skill' => $request->skill


            
        ]);

        
    }

